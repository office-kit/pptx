// Turns the enrichment-workflow output (/tmp/enrich-result.json) into
// `overrides.generated.ts`. Validates every ParamSpec kind and — critically —
// that each override's TOP-LEVEL parameter names match the generated
// capability exactly (the registry dispatches positionally by those names, so a
// mismatch would silently pass `undefined`). Nested field names are trusted as
// the agent read them from the library source.
import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const RESULT = process.argv[2] ?? '/tmp/enrich-result.json';
const generated = JSON.parse(readFileSync(join(here, 'capabilities.generated.json'), 'utf8'));
const byId = new Map(generated.capabilities.map((c) => [c.id, c]));
const VALID_KINDS = new Set([
  'string',
  'number',
  'emu',
  'color',
  'boolean',
  'enum',
  'index',
  'object',
  'array',
]);

const data = JSON.parse(readFileSync(RESULT, 'utf8'));

function validateSpec(spec, path, errors) {
  if (!spec || typeof spec !== 'object') return errors.push(`${path}: not an object`);
  if (!spec.name) errors.push(`${path}: missing name`);
  if (!VALID_KINDS.has(spec.kind)) errors.push(`${path}.${spec.name}: invalid kind "${spec.kind}"`);
  if (spec.kind === 'enum' && (!Array.isArray(spec.enumValues) || !spec.enumValues.length)) {
    errors.push(`${path}.${spec.name}: enum without enumValues`);
  }
  if (spec.fields) for (const f of spec.fields) validateSpec(f, `${path}.${spec.name}`, errors);
  if (spec.item) validateSpec(spec.item, `${path}.${spec.name}[]`, errors);
}

// Normalize a spec into the exact ParamSpec field set (drop stray keys).
function clean(spec) {
  const out = {
    name: String(spec.name),
    type: String(spec.type ?? 'unknown'),
    kind: spec.kind,
    optional: Boolean(spec.optional),
  };
  if (spec.default !== undefined) out.default = String(spec.default);
  if (spec.label) out.label = String(spec.label);
  if (spec.kind === 'enum' && spec.enumValues) out.enumValues = spec.enumValues.map(String);
  if (spec.fields) out.fields = spec.fields.map(clean);
  if (spec.item) out.item = clean(spec.item);
  return out;
}

const skipped = [];
const entries = {};
for (const o of data.overrides) {
  const errors = [];
  const cap = byId.get(o.id);
  if (!cap) {
    skipped.push(`${o.id}: not a known capability`);
    continue;
  }
  let params;
  try {
    params = JSON.parse(o.paramsJson);
  } catch (e) {
    skipped.push(`${o.id}: bad paramsJson (${e.message})`);
    continue;
  }
  if (!Array.isArray(params)) {
    skipped.push(`${o.id}: paramsJson not an array`);
    continue;
  }
  // Top-level param names must equal the generated user-param names, in order —
  // else the registry would pass the wrong (or undefined) positional args.
  const wantNames = cap.params.map((p) => p.name);
  const gotNames = params.map((p) => p.name);
  if (JSON.stringify(wantNames) !== JSON.stringify(gotNames)) {
    skipped.push(
      `${o.id}: param names ${JSON.stringify(gotNames)} != expected ${JSON.stringify(wantNames)}`,
    );
    continue;
  }
  for (const p of params) validateSpec(p, o.id, errors);
  if (errors.length) {
    skipped.push(...errors);
    continue;
  }
  entries[o.id] = {
    labelEn: o.labelEn,
    labelJa: o.labelJa,
    params: params.map(clean),
  };
}

if (skipped.length) {
  // Skipped entries keep their generated (JSON-fallback) schema — still reachable.
  console.warn('SKIPPED (kept on generated schema):\n  ' + skipped.join('\n  '));
}

const banner = `// AUTO-GENERATED by build-generated-overrides.mjs from an enrichment pass that
// read the @office-kit/pptx type sources. Provides field-based parameter schemas
// (accurate enum members, nested object/array fields) so the command dialogs are
// forms rather than JSON blobs. Do not edit by hand — re-run the generator.
// Human tweaks belong in overrides.ts, which is merged on top of these.
import type { CapabilityOverride } from './types.ts';

export const generatedOverrides: Record<string, CapabilityOverride> = ${JSON.stringify(entries, null, 2)};
`;

const outPath = join(here, 'overrides.generated.ts');
writeFileSync(outPath, banner);
console.log(
  `Wrote ${Object.keys(entries).length} generated overrides to ${outPath.replace(here + '/', '')}`,
);
